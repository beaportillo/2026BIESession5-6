# Arithmetic operators (+, -, etc)
print("2+4=", 2+4)
print("8/2=", 8/2)

print("9%2=", 9%2)      # El resto
print(99%100)
print(10 % 3)   # 1  (because 10 รท 3 = 3 remainder 1)
print(8 % 2)    # 0  (divides evenly)
print(7 % 4)    # 3

print("8//2=", 8//2)    # Integer division
print("3**4=", 3**4)    # Exponential
print(abs(-1))          # Absolute
